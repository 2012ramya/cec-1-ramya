#include<stdio.h>
void main(){
    int num1 =10;
    int num2 = 20;
    int sum =num1 + num2;

 
     printf(" sum of %d & %d :%d%d\n", num1,num2,sum);
     printf(" difference of %d & %d : %d\n", num1 ,num2,num1 - num2);
     printf("product of %d & %d : %d\n", num1 ,num2,num1 * num2);
     printf("quotient of %d & %d : %d\n", num1 ,num2,num1 / num2);
     // modulus or reminder 
     printf("Remainder of %d & %d : %d\n", num1 ,num2, num1 % num2);
}